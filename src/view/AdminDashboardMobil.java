    private void cariButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariButtonActionPerformed
        String keyword = searchTextField.getText().trim();
    
    if (keyword.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan kata kunci pencarian");
        return;
    }
    
    // Panggil method search dari controller
    List<Mobil> hasilPencarian = controller.cariMobil(keyword);
    
    // Tampilkan hasil pencarian di tabel
    tampilkanHasilPencarian(hasilPencarian);
    clearForm();
    }//GEN-LAST:event_cariButtonActionPerformed
    
    private void tampilkanHasilPencarian(List<Mobil> hasilPencarian) {
    DefaultTableModel model = (DefaultTableModel) tblMobil.getModel();
    model.setRowCount(0); // Bersihkan tabel
    
    if (hasilPencarian.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Tidak ditemukan data mobil yang sesuai");
        return;
    }
    
    for (Mobil m : hasilPencarian) {
        String statusStr = convertIntToStatus(m.getStatus());
        model.addRow(new Object[] {
            m.getPlat(), 
            m.getTipe(), 
            m.getMerk(), 
            m.getHargaSewa(), 
            statusStr
        });
    }
    }
    
    private void tblMobilMouseClicked(java.awt.event.MouseEvent evt) {
        int selectedRow = tblMobil.getSelectedRow();
        if (selectedRow >= 0) {
            platTextField.setText(tblMobil.getValueAt(selectedRow, 0).toString());
            tipeTextField.setText(tblMobil.getValueAt(selectedRow, 1).toString());
            merkTextField.setText(tblMobil.getValueAt(selectedRow, 2).toString());
            hargaTextField.setText(tblMobil.getValueAt(selectedRow, 3).toString());
            
            String status = tblMobil.getValueAt(selectedRow, 4).toString();
            cmbStatus.setSelectedItem(status);
            
            platTextField.setEnabled(false);
        }
    }


    
