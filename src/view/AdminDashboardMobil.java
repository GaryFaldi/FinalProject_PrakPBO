    private void initTable() {
        // Buat model tabel dengan kolom yang sesuai
        tableModel = new DefaultTableModel();
        tableModel.addColumn("Plat Nomor");
        tableModel.addColumn("Tipe");
        tableModel.addColumn("Merk");
        tableModel.addColumn("Harga Sewa");
        tableModel.addColumn("Status");
        
        tblMobil.setModel(tableModel);
    }
    private void loadData() {
        // Ambil data dari controller
        List<Mobil> daftarMobil = controller.getAllMobil();
        
        // Bersihkan tabel
        tableModel.setRowCount(0);
        
        // Isi tabel dengan data baru
        for (Mobil m : daftarMobil) {
            String statusStr = convertIntToStatus(m.getStatus());
            tableModel.addRow(new Object[] {
                m.getPlat(), 
                m.getTipe(), 
                m.getMerk(), 
                m.getHargaSewa(), 
                statusStr
            });
        }
    }


    private int convertStatusToInt(String statusText) {
        return statusText.equals("Tersedia") ? 1 : 0;
    }

    private String convertIntToStatus(int statusValue) {
        return statusValue == 1 ? "Tersedia" : "Disewa";
    }

    
    public void setTableData(List<Mobil> daftarMobil) {
        DefaultTableModel model = (DefaultTableModel) tblMobil.getModel();
        model.setRowCount(0);
        for (Mobil m : daftarMobil) {
            String statusStr = convertIntToStatus(m.getStatus());
            model.addRow(new Object[] {
            m.getPlat(), 
            m.getTipe(), 
            m.getMerk(), 
            m.getHargaSewa(), 
            statusStr  // Gunakan string status
        });
        }
    }
    
    
    
