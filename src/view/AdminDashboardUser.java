    private void initTable() {
        tableModel = new DefaultTableModel();
        tableModel.addColumn("ID");
        tableModel.addColumn("Username");
        tableModel.addColumn("Password");
        tableModel.addColumn("Role");
        tblUser.setModel(tableModel);
    }
    
    private void loadData() {
        List<User> daftarUser = controller.getAllUser();
        tableModel.setRowCount(0);
        
        for (User u : daftarUser) {
            tableModel.addRow(new Object[] {
                u.getId(),
                u.getUsername(),
                u.getPassword(),
                u.getRole()
            });
        }
    }
    
    private void clearForm() {
        idTextField.setText("");
        usernameTextField.setText("");
        passwordTextField.setText("");
        cmbRole.setSelectedIndex(0);
        idTextField.setEnabled(false);
        searchTextField.setText("");
    }
    
    private void tblUserMouseClicked(java.awt.event.MouseEvent evt) {
        int selectedRow = tblUser.getSelectedRow();
        if (selectedRow >= 0) {
            idTextField.setText(tblUser.getValueAt(selectedRow, 0).toString());
            usernameTextField.setText(tblUser.getValueAt(selectedRow, 1).toString());
            passwordTextField.setText(tblUser.getValueAt(selectedRow, 2).toString());
            cmbRole.setSelectedItem(tblUser.getValueAt(selectedRow, 3).toString());
            
            idTextField.setEnabled(false);
        }
    }

