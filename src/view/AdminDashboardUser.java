    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        if (idTextField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih user yang akan diupdate terlebih dahulu");
            return;
        }
        
        int id = Integer.parseInt(idTextField.getText());
        String username = usernameTextField.getText();
        String password = passwordTextField.getText();
        String role = cmbRole.getSelectedItem().toString();
        
        User user = new User(id, username, password, role);
        controller.updateUser(user);
        
        JOptionPane.showMessageDialog(this, "Data user berhasil diupdate");
        loadData();
        clearForm();
    }//GEN-LAST:event_updateButtonActionPerformed

    private void hapusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusButtonActionPerformed
       if (idTextField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih user yang akan dihapus terlebih dahulu");
            return;
        }
        
        int id = Integer.parseInt(idTextField.getText());
        
        int confirm = JOptionPane.showConfirmDialog(this,
            "Yakin ingin menghapus user dengan ID " + id + "?",
            "Konfirmasi Hapus",
            JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            controller.hapusUser(id);
            JOptionPane.showMessageDialog(this, "User berhasil dihapus");
            loadData();
            clearForm();
        }
    }//GEN-LAST:event_hapusButtonActionPerformed

